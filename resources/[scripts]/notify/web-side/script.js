window.addEventListener('message', function(event) {
    const item = event.data;

    if (item.name === 'Notify') {
        const payload = item.payload;
        const type = payload[0] || 'default';
        const message = payload[1];
        const title = payload[2] || type.charAt(0).toUpperCase() + type.slice(1); // Default title if null
        const time = payload[3] || 5000;

        addNotification(type, title, message, time);
    }
});

function addNotification(type, title, message, time) {
    const container = document.getElementById('notifications-container');
    const notification = document.createElement('div');
    notification.classList.add('notification', type);
    
    // Check if we have a specific icon for this type, otherwise use default
    const iconSvg = icons[type] || icons['default'];
    // Check if we have css class for this type, otherwise use default
    if (!icons[type]) notification.classList.add('default');

    notification.innerHTML = `
        <div class="icon-container">
            ${iconSvg}
        </div>
        <div class="content">
            <div class="title">${title}</div>
            <div class="message">${message}</div>
        </div>
        <div class="progress-bar">
            <div class="progress-fill"></div>
        </div>
    `;

    container.appendChild(notification);

    // Animate progress bar
    const progressFill = notification.querySelector('.progress-fill');
    progressFill.style.transition = `transform ${time}ms linear`;
    
    // Trigger reflow
    void progressFill.offsetWidth;
    
    progressFill.style.transform = 'scaleX(0)';

    setTimeout(() => {
        notification.classList.add('outro');
        notification.addEventListener('animationend', () => {
            if (notification.parentNode) {
                notification.parentNode.removeChild(notification);
            }
        });
    }, time);
}
